public class Boss {
    private int bossHealth;
    private int bossAttack;
    private String bossSheild;

    public int getBossHealth() {
        return bossHealth;
    }

    public void setBossHealth(int bossHealth) {
        this.bossHealth = bossHealth;
    }

    public int getBossAttack() {
        return bossAttack;
    }

    public void setBossAttack(int bossAttack) {
        this.bossAttack = bossAttack;
    }

    public String getBossSheild() {
        return bossSheild;
    }

    public void setBossSheild(String bossSheild) {
        this.bossSheild = bossSheild;
    }
}